package day17_whileLoops;

public class looppractices {
    public static void main(String[] args) {
        for (int i = 0; i <= 10; i++) {
            System.out.println(i);

        }
        int j = 1;
        while (j <= 10) {
            System.out.println(j);
            j++;

        }

        int k = 1;
        do {
            System.out.println(k);
            k++;
        }while (k<=10);

    }
}
